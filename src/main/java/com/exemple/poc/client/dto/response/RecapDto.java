package com.exemple.poc.client.dto.response;

public class RecapDto {
	
	private long nbTeams;
	private long nbPlayers;
	private long nbCompetitions;
	private long nbPhasis;
	private long nbRosters;
	private long nbGames;
	
	public long getNbTeams() {
		return nbTeams;
	}
	public void setNbTeams(long nbTeams) {
		this.nbTeams = nbTeams;
	}
	public long getNbPlayers() {
		return nbPlayers;
	}
	public void setNbPlayers(long nbPlayers) {
		this.nbPlayers = nbPlayers;
	}
	public long getNbCompetitions() {
		return nbCompetitions;
	}
	public void setNbCompetitions(long nbCompetitions) {
		this.nbCompetitions = nbCompetitions;
	}
	public long getNbPhasis() {
		return nbPhasis;
	}
	public void setNbPhasis(long nbPhasis) {
		this.nbPhasis = nbPhasis;
	}
	public long getNbRosters() {
		return nbRosters;
	}
	public void setNbRosters(long nbRosters) {
		this.nbRosters = nbRosters;
	}
	public long getNbGames() {
		return nbGames;
	}
	public void setNbGames(long nbGames) {
		this.nbGames = nbGames;
	}
	


}
